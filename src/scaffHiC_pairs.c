/*   subroutine to sort out read pairs    */
/* =============================== */
void Matrix_Process(char **argv,int args,int nSeq)
/* =============================== */
{
     int i,j,k,m,n,n_scaff,n_blocks;
     FILE *namef,*namef2;
     int *ctg_list;
     long num_cells,n_Bases;
     int num_hits,num_hit1,num_hit2,rcdex,rsize,rsize2,size_row;
     int stopflag,offset,*ray,*dex;
     int *ctg_idex1,*ctg_idex2,*ctg_mask,*ctg_rcdex1;
     int n_length = min_len;

     rsize = max_ctg+10; 
     n_blocks = rsize*rsize; 
     if((ctg_idex1 = (int *)calloc(nSeq,sizeof(int))) == NULL)
     {
       printf("fmate: calloc - ctg_idex1\n");
       exit(1);
     }
     if((ctg_idex2 = (int *)calloc(nSeq,sizeof(int))) == NULL)
     {
       printf("fmate: calloc - ctg_idex2\n");
       exit(1);
     }

     printf("contigs: %d %d %d\n",rsize,max_ctg,nSeq);
     num_hits =0;
     k = 0;
     offset = 0;
     for(i=0;i<nSeq;i++)
     {
        stopflag=0;
        j=i+1;
        while((j<nSeq)&&(stopflag==0))
        {
          if(strcmp(R_Name[i],R_Name[j])==0)
          {
            j++;
          }
          else
            stopflag=1;
        }
        if((j-i)>=2) 
        {
          int idi,idt,len1,len2,loci1,loci2;

          if((hit_score[i] > n_mapq)&&(hit_score[i+1] > n_mapq))
          {
            if((hit_index[i] == i_ctg1)&&(hit_index[i+1] == i_ctg2))
            {
              ctg_idex1[i] = 1;
              ctg_idex2[i+1] = 1;
            }
            else if((hit_index[i] == i_ctg2)&&(hit_index[i+1] == i_ctg1))
            {
              ctg_idex1[i+1] = 1;
              ctg_idex2[i] = 1;
            }
          }
        }
        else
        {
          printf("www: %s %d\n",R_Name[i],superlength[i]);
        }
        i=j-1;
     }

     if((namef = fopen(argv[args+1],"w")) == NULL)
     {
       printf("ERROR main:: alignment file 2 \n");
       exit(1);
     }

     if((namef2 = fopen(argv[args+2],"w")) == NULL)
     {
       printf("ERROR main:: alignment file 2 \n");
       exit(1);
     }

     for(i=0;i<nSeq;i++)
     {
        if(ctg_idex1[i] == 1)
          fprintf(namef,"%s %d\n",R_Name[i],hit_locus1[i]);
     }
     for(i=0;i<nSeq;i++)
     {
        if(ctg_idex2[i] == 1)
          fprintf(namef2,"%s %d\n",R_Name[i],hit_locus1[i]);
     }
     fclose(namef);
     fclose(namef2);

     exit(1);
}


#define SWAP(a,b) temp=(a);(a)=b;(b)=temp;

/*   Subroutine to sort an array arr[0,...,n-1] into ascending order while
     making the corresponding reaarangement of the array brr[0,...,n-1]
     by the use of Quicksort (Sedgwick, R. 1978, Communications o fthe ACM,
     vol. 21, pp. 847-857) also see Numerical Recipes in C                  */  

/* =============================== */
void ArraySort_Long(int n, long *arr)
/* =============================== */
{
     int i,ir=n-1,j,k,m=0,jstack=0,NSTACK=50,istack[NSTACK];
     long a,temp,MIN=7;

     for(;;)
     {
/*      Insertion sort when subarray is small enough    */
        if(ir-m<MIN)
        {
          for(j=m+1;j<=ir;j++)
          {
             a=arr[j];
             for(i=j-1;i>=m;i--)
             {
                if(arr[i]<=a) break;
                arr[i+1]=arr[i];
             }
             arr[i+1]=a;
          }
          if(!jstack) return;
          ir=istack[jstack--];
          m=istack[jstack--];
        }
        else
        {
          k=(m+ir)>>1;
          SWAP(arr[k],arr[m+1]);

          if(arr[m]>arr[ir])
          {
            SWAP(arr[m],arr[ir]);
          }

          if(arr[m+1]>arr[ir])
          {
            SWAP(arr[m+1],arr[ir]);
          }

          if(arr[m]>arr[m+1])
          {
            SWAP(arr[m],arr[m+1]);
          }

          i=m+1;
          j=ir;
          a=arr[m+1];
          for(;;)
          {
             do i++; while (arr[i]<a);
             do j--; while (arr[j]>a);
             if(j<i) break;
             SWAP(arr[i],arr[j]);
          }
          arr[m+1]=arr[j];
          arr[j]=a;
          jstack+=2;

/*        Push pointers to larger subarray on stack      */
/*        process smaller subarray immediately           */
          if(jstack>NSTACK)
          {
             printf("Stack error: NSTACK too small\n");
             exit(0);
          }
          if(ir-i+1>=j-m)
          {
            istack[jstack]=ir;
            istack[jstack-1]=i;
            ir=j-1;
          }
          else
          {
            istack[jstack]=j-1;
            istack[jstack-1]=m;
            m=i;
          }
        }
     }
}


